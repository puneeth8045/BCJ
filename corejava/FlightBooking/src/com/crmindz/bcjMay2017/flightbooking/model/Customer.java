package com.crmindz.bcjMay2017.flightbooking.model;

public class Customer {
	private String firstName;
	private String lastName;
	private String email;
	private String phnNo;
	private String accountNo;
	private String save;
	private Address address;
	private BookingDetails bd;
	private FlightDetails fd;
	private LoginCredentails login;
	private FuturePaymentDetails spd;
	private CardDetails cd;

	public String getSave() {
		return save;
	}

	public void setSave(String save) {
		this.save = save;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPhnNo() {
		return phnNo;
	}

	public void setPhnNo(String phnNo) {
		this.phnNo = phnNo;
	}
	public String getAccountNo() {
		return accountNo;
	}

	public void setAccountNo(String accountNo) {
		this.accountNo = accountNo;
	}

	public Address getAddress() {
		return address;
	}

	public void setAddress(Address address) {
		this.address = address;
	}

	public BookingDetails getBd() {
		return bd;
	}

	public void setBd(BookingDetails bd) {
		this.bd = bd;
	}

	public FlightDetails getFd() {
		return fd;
	}

	public void setFd(FlightDetails fd) {
		this.fd = fd;
	}

	public LoginCredentails getLogin() {
		return login;
	}

	public void setLogin(LoginCredentails login) {
		this.login = login;
	}

	public FuturePaymentDetails getSpd() {
		return spd;
	}

	public void setSpd(FuturePaymentDetails spd) {
		this.spd = spd;
	}

	public CardDetails getCd() {
		return cd;
	}

	public void setCd(CardDetails cd) {
		this.cd = cd;
	}

}
