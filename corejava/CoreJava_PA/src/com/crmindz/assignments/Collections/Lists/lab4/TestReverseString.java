package com.crmindz.assignments.Collections.Lists.lab4;

import static org.junit.Assert.*;

import java.util.ArrayList;

import org.junit.Test;

public class TestReverseString {
	ReverseStrings rvsstrings = new ReverseStrings();
	
	
	@Test
	public void test() {
		StringBuffer sb = new StringBuffer("woh");
		StringBuffer sb1 = new StringBuffer("era");
		StringBuffer sb2 = new StringBuffer("uoy");

		ArrayList<StringBuffer> ouputStrings = new ArrayList<StringBuffer>();

		ouputStrings.add(sb);
		ouputStrings.add(sb1);
		ouputStrings.add(sb2);

		assertEquals(ouputStrings, rvsstrings.storedValues(sb, sb1, sb2));
	}

}
