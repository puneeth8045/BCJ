package com.crmindz.assignments.Oops.AbstractClasses.lab1;

/**
 * @author Puneeth This is a abstract class of LandMammals which contains the
 *         abstract and concrete methods of them.
 */
public abstract class LandMammal extends Mammal {
	/**
	 * Abstract method of walking for Land Mammals.
	 */
	abstract void walk();

	/**
	 * Abstract method of running for Land Mammals.
	 */
	abstract void run();

	/**
	 * Abstract method of crawling for Land Mammals.
	 */
	abstract void crawl();
}
