package com.crmindz.assignments.Oops.Inheritance.lab6;

/**
 * @author Puneeth This class will inherit the Student class.
 */
public class DayScholar extends Student {
	Address residentialAddress;
	float distance;

	/**
	 * @param studentId
	 * @param studentName
	 * @param qualifyingExamMarks
	 * @param residentialStatus
	 * @param branchName
	 * @param yearOfEngg
	 * @param permanentAddress
	 * @param residentialAddress
	 * @param distance
	 */
	public DayScholar(int studentId, String studentName, float qualifyingExamMarks, char residentialStatus,
			String branchName, int yearOfEngg, Address permanentAddress, Address residentialAddress, float distance) {
		super(studentId, studentName, qualifyingExamMarks, residentialStatus, branchName, yearOfEngg, permanentAddress);
		this.residentialAddress = residentialAddress;
		this.distance = distance;
	}

	public Address getResidentialAddress() {
		return residentialAddress;
	}

	public void setResidentialAddress(Address residentialAddress) {
		this.residentialAddress = residentialAddress;
	}

	public float getDistance() {
		return distance;
	}

	public void setDistance(float distance) {
		this.distance = distance;
	}

}
