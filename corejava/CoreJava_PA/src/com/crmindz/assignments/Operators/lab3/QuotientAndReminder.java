package com.crmindz.assignments.Operators.lab3;

import java.util.Scanner;

/**
 * @author Puneeth This class will calculate and display the quotient and
 *         remainder for two given inputs.
 */
public class QuotientAndReminder {
	/**
	 * @param n1
	 * @param n2
	 *            * Ths method will take two inputs and calculate the quotient
	 *            and remainder.
	 */
	public void getQuotientAndReminder(int n1, int n2) {
		int q = n1 / n2;
		int r = n1 % n2;
		System.out.println("The quotient is: " + q + "\t" + "The reminder is: " + r);
	}

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.print("Enter the number1: ");
		int n1 = sc.nextInt();
		System.out.print("Enter the number2:");
		int n2 = sc.nextInt();
		QuotientAndReminder qr = new QuotientAndReminder();
		qr.getQuotientAndReminder(n1, n2);
		sc.close();
		// TODO Auto-generated method stub

	}

}
