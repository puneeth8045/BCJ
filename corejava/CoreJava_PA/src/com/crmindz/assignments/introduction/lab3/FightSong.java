package com.crmindz.assignments.introduction.lab3;

/**
 * @author Puneeth
 *This class prints the fight song by using static methods.
 *
 */
public class FightSong {
	/**
	 * this methods prints first chorus of fight song
	 */
	public static void getLyrics1(){
		System.out.println("Go, team, go! \n"+"You can do it.");
	}
	/**
	 * This method prints second chorus of fight song
	 */
	public static void getLyrics2(){
		FightSong.getLyrics1();
		System.out.println("You're the best, \n"+"In the West.");
		FightSong.getLyrics1();
	}
	/**
	 * This method prints new line
	 */
	public static void giveSpace(){
		System.out.println("\n");
	}

	public static void main(String[] args) {
		FightSong.getLyrics1();
		
		for(int i=0;i<2;i++){
			FightSong.giveSpace();
			FightSong.getLyrics2();
		}
		
		FightSong.giveSpace();
		FightSong.getLyrics1();

	}
	

}
